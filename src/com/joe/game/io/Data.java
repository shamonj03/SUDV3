package com.joe.game.io;

public class Data {
	/**
	 * The unique ID of the data class.
	 */
	private int id;

	/**
	 * Creates a new Data instance.
	 * 
	 * @param id
	 *            The unique id of the data class.
	 */
	public Data(int id) {
		this.id = id;
	}

	/**
	 * @return the unique id of the data class.
	 */
	public int getId() {
		return id;
	}

}
